# ─────────────────────────────────────────────────────────
# SUMMARY
# ─────────────────────────────────────────────────────────
function Make-Summary {
  param([string]$OutRootLocal)

  function Get-Col {
    param($r, [string[]]$names)
    foreach ($n in $names) {
      if ($r.PSObject.Properties.Name -contains $n -and $r.$n -ne $null -and "$($r.$n)".Length -gt 0) {
        return $r.$n
      }
    }
    return $null
  }

  function Infer-BaselineFromTag([string]$tag) {
    if (-not $tag) { return "" }
    $m = [regex]::Match($tag, '(4pct|cpb|vpw|kgr)', 'IgnoreCase')
    if ($m.Success) { return $m.Groups[1].Value.ToLower() }
    return ""
  }

  $cleanPath  = Join-Path $OutRootLocal "night_summary_clean.csv"
  $reportPath = Join-Path $OutRootLocal "night_summary_report.csv"

  $metrics = Get-ChildItem -Path $OutRootLocal -Recurse -Filter "metrics.csv"
  if ($metrics.Count -eq 0) { Write-Warning "metrics.csv 없음 - 먼저 배치를 실행하세요."; return }

  $rows = @()
  foreach ($m in $metrics) {
    try {
      $csv = Import-Csv $m.FullName
      foreach ($r in $csv) {
        $full = $m.DirectoryName

        # w_fixed 라벨 (폴더명에서 추출, 없으면 NA)
        $w_label = ($full -split "[\\\/]") | Where-Object { $_ -like "w_*" } | Select-Object -Last 1
        if (-not $w_label) { $w_label = "NA" }

        # method
        $methodVal = ""; if ($r.PSObject.Properties.Name -contains "method") { $methodVal = [string]$r.method }
        if ($methodVal -in @("hjb","rl")) { $w_label = "NA" }

        # baseline: CSV 없으면 tag에서 유추
        $baselineCsv = ""; if ($r.PSObject.Properties.Name -contains "baseline" -and $r.baseline) { $baselineCsv = [string]$r.baseline }
        $tagVal = Get-Col $r @("tag")
        $baselineVal = if ($baselineCsv) { $baselineCsv } else { Infer-BaselineFromTag ($tagVal) }

        # seed/elapsed
        $seedVal = ""; foreach ($cand in @("seed","seed_id","seed_idx")) {
          if ($r.PSObject.Properties.Name -contains $cand -and $r.$cand) { $seedVal = [string]$r.$cand; break }
        }
        $elapsedVal = ""; foreach ($cand in @("time_total_hms","elapsed_hms","elapsed")) {
          if ($r.PSObject.Properties.Name -contains $cand -and $r.$cand) { $elapsedVal = [string]$r.$cand; break }
        }

        # 값 폴백: EW / ES95 / Ruin / mean_WT (안전 캐스팅)
        $EWv    = To-DoubleOrNull (Get-Col $r @("EW","mean_WT"))
        $ES95v  = To-DoubleOrNull (Get-Col $r @("ES95","ES95_loss"))
        $Ruinv  = To-DoubleOrNull (Get-Col $r @("Ruin","RuinPct"))
        $WTv    = To-DoubleOrNull (Get-Col $r @("mean_WT","EW"))

        $rows += [pscustomobject]@{
          tag      = $tagVal
          method   = $methodVal
          baseline = $baselineVal
          w_fixed  = $w_label
          seed     = $seedVal
          EW       = $EWv
          ES95     = $ES95v
          RuinPct  = $Ruinv
          mean_WT  = $WTv
          elapsed  = $elapsedVal
          out_dir  = $full

          # 추가 필드(있으면 반영)
          es_mode        = Get-Col $r @("es_mode")
          AlivePathRate  = Get-Col $r @("AlivePathRate")
          C_ES95_avg     = Get-Col $r @("C_ES95_avg")
          p10_c_last     = Get-Col $r @("p10_c_last")
          p50_c_last     = Get-Col $r @("p50_c_last")
          p90_c_last     = Get-Col $r @("p90_c_last")
        }
      }
    } catch {
      Write-Warning "Import 실패: $($m.FullName)  $_"
    }
  }

  # 결정성 정렬 + 중복 제거
  $rows = $rows | Sort-Object tag,method,baseline,w_fixed,seed,out_dir -Unique
  $rows | Export-Csv -NoTypeInformation -Encoding UTF8 $cleanPath
  Write-Host "  -> $cleanPath"

  $report =
    $rows |
    Group-Object method, baseline, w_fixed |
    ForEach-Object {
      $g = $_.Group
      # 평균 시 Null 제외
      $EW_vals   = $g | Where-Object { $_.EW      -ne $null } | Select-Object -ExpandProperty EW
      $ES95_vals = $g | Where-Object { $_.ES95    -ne $null } | Select-Object -ExpandProperty ES95
      $Ruin_vals = $g | Where-Object { $_.RuinPct -ne $null } | Select-Object -ExpandProperty RuinPct
      $WT_vals   = $g | Where-Object { $_.mean_WT -ne $null } | Select-Object -ExpandProperty mean_WT

      [pscustomobject]@{
        method   = $g[0].method
        baseline = $g[0].baseline
        w_fixed  = $g[0].w_fixed
        seed_n   = $g.Count
        EW_avg   = if($EW_vals){ [double]("{0:N6}" -f ((($EW_vals | Measure-Object -Average).Average))) } else { $null }
        ES95_avg = if($ES95_vals){ [double]("{0:N6}" -f ((($ES95_vals | Measure-Object -Average).Average))) } else { $null }
        Ruin_avg = if($Ruin_vals){ [double]("{0:N6}" -f ((($Ruin_vals | Measure-Object -Average).Average))) } else { $null }
        WT_avg   = if($WT_vals){ [double]("{0:N6}" -f ((($WT_vals | Measure-Object -Average).Average))) } else { $null }
      }
    } | Sort-Object method, baseline, w_fixed

  $report | Export-Csv -NoTypeInformation -Encoding UTF8 $reportPath
  Write-Host "  -> $reportPath"
}

# ─────────────────────────────────────────────────────────
# MAIN (단일 빌드 경로)
# ─────────────────────────────────────────────────────────
Validate-Inputs

function Build-And-Run {
  $global:RunPlan = @()
  if ($Method -eq "rule") {
    $root = Join-Path $script:OutRoot "w_fixed"
    foreach ($w in $WList) {
      $wLabel = ("w_{0}" -f ($w.ToString().Replace('.','_')))
      $outDir = Join-Path $root $wLabel
      Add-Run -Tag ("rule_w{0}" -f $w) -OutDir $outDir -Fixed @{ "w_fixed" = $w }
    }
  } elseif ($Method -eq "hjb") {
    $root = Join-Path $script:OutRoot "hjb"
    $tag2 = if ($Tag) { $Tag } else { "hjb_base" }
    $outDir = Join-Path $root $tag2
    Add-Run -Tag $tag2 -OutDir $outDir -Fixed @{}
  } else {
    $root = Join-Path $script:OutRoot "rl"
    $tag2 = if ($Tag) { $Tag } else { "rl_base" }
    $outDir = Join-Path $root $tag2
    Add-Run -Tag $tag2 -OutDir $outDir -Fixed @{}
  }

  $global:RunStats.Total = $global:RunPlan.Count
  Write-Host ("[PLAN] total runs = {0} (Method={1})" -f $global:RunStats.Total, $Method)

  foreach ($job in $global:RunPlan) {
    Invoke-Run -Tag $job.Tag -OutDir $job.OutDir -Fixed $job.Fixed
  }

  Write-Host ("[SUMM] Ok={0} Fail={1} Done={2}/{3} (started {4})" -f `
    $global:RunStats.Ok, $global:RunStats.Fail, $global:RunStats.Done, $global:RunStats.Total, `
    $global:RunStats.StartedAt.ToString("yyyy-MM-dd HH:mm:ss"))
}

Build-And-Run

if ($DoSummary) {
  Write-Host ">> 요약집계 생성 시작"
  Make-Summary -OutRootLocal $script:OutRoot
  Write-Host ">> 요약집계 완료"
}
