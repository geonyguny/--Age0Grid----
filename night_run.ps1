# ─────────────────────────────────────────────────────────
# 유틸
# ─────────────────────────────────────────────────────────
function Ensure-Dir($p) { if (!(Test-Path $p)){ New-Item -ItemType Directory -Force -Path $p | Out-Null } }

function Resolve-AbsPath([string]$p) {
  if ([string]::IsNullOrWhiteSpace($p)) { return $p }
  $rp = Resolve-Path -Path $p -ErrorAction SilentlyContinue
  if ($rp) { return $rp.Path }
  return (Join-Path (Get-Location) $p)
}

function Ensure-Python {
  $pyAbs = Resolve-AbsPath $Py
  if (-not (Test-Path $pyAbs)) { throw "Python not found: $pyAbs" }
  return $pyAbs
}

function Warn-If($cond,$msg){ if($cond){ Write-Warning $msg } }

# 안전한 double 캐스팅
function To-DoubleOrNull($v){
  if ($null -eq $v) { return $null }
  $s = "$v".Trim()
  if ($s.Length -eq 0) { return $null }
  [double]$out = 0.0
  if ([double]::TryParse($s, [System.Globalization.NumberStyles]::Any,
      [System.Globalization.CultureInfo]::InvariantCulture, [ref]$out)) {
    return $out
  }
  return $null
}

# Python shim 생성 (모듈 진입 + 폴백)
function Ensure-Shim {
  $shimDir = ".\.scripts"
  Ensure-Dir $shimDir
  $shimPath = Join-Path $shimDir "cli_shim.py"
  @"
# auto-generated by night_run.ps1 (refactor v9)
import sys, os, runpy

DROP_OPTS = {"--dump_eval", "--dump-eval"}

def strip_unknown(argv):
    out, skip = [], 0
    for i, tok in enumerate(argv):
        if skip:
            skip -= 1; continue
        low = str(tok).lower()
        if low in DROP_OPTS:
            if i+1 < len(argv) and not str(argv[i+1]).startswith("-"):
                skip = 1
            continue
        out.append(tok)
    return out

def try_run(module):
    try:
        runpy.run_module(module, run_name="__main__")
        return True
    except ModuleNotFoundError as e:
        sys.stderr.write(f"[shim] ModuleNotFoundError: {e}\\n")
        return False

if __name__ == "__main__":
    if len(sys.argv) < 2:
        raise SystemExit("usage: cli_shim.py <module> [args...]")
    module = sys.argv[1]
    repo_root = os.path.abspath(os.path.join(os.path.dirname(__file__), os.pardir))
    if repo_root not in sys.path:
        sys.path.insert(0, repo_root)
    sys.argv = [sys.argv[0]] + strip_unknown(sys.argv[2:])
    if not try_run(module):
        # fallback 모듈명 (runner.cli)
        fb = "runner.cli"
        sys.stderr.write(f"[shim] fallback to {fb}\\n")
        try_run(fb)
"@ | Set-Content -Path $shimPath -Encoding UTF8
  return (Resolve-AbsPath $shimPath)
}

