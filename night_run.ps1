$global:RunPlan   = @()
$global:RunStats  = [ordered]@{ StartedAt = (Get-Date); Total=0; Done=0; Fail=0; Ok=0; Durations=@() }

function Append-DecisionLog {
  param([string]$OutRootLocal,[hashtable]$Row)
  $logDir = Join-Path $OutRootLocal "_logs"
  Ensure-Dir $logDir
  $logCsv = Join-Path $logDir "decision_log.csv"

  $order = @("timestamp","method","tag","outdir","rc","took_s","took_hms",
             "seeds","seed_list","n_paths","module","commit","version")
  $obj = New-Object psobject
  foreach($k in $order){ $null = Add-Member -InputObject $obj -MemberType NoteProperty -Name $k -Value ($Row[$k]) }

  if (!(Test-Path $logCsv)) {
    $obj | Export-Csv -Path $logCsv -NoTypeInformation -Encoding UTF8
  } else {
    $obj | Export-Csv -Path $logCsv -NoTypeInformation -Append -Encoding UTF8
  }
}

function Print-ETA {
  $done = [double]$global:RunStats.Done
  $total = [double]$global:RunStats.Total
  if ($done -le 0 -or $global:RunStats.Durations.Count -eq 0) { return }
  $avg = ($global:RunStats.Durations | Measure-Object -Average).Average
  $remaining = $total - $done
  $etaSec = [int]([math]::Max(0, $remaining * $avg))
  $eta = (Get-Date).AddSeconds($etaSec)
  Write-Host ("[ETA ] {0}/{1} avg={2}s eta={3}" -f $done, $total, [int]$avg, $eta.ToString("yyyy-MM-dd HH:mm:ss"))
}

# ─────────────────────────────────────────────────────────
# Validate 입력 조합/환경
# ─────────────────────────────────────────────────────────
function Validate-Inputs {
  $pyAbs = Ensure-Python

  # OutRoot 절대경로 고정(결정성)
  $script:OutRoot = Resolve-AbsPath $OutRoot
  Ensure-Dir $script:OutRoot

  # 조합 검증
  if ($Method -eq "rule") {
    if (-not $WList -or $WList.Count -eq 0) { throw "Method=rule requires non-empty -WList." }
    $WList = @([double[]]($WList | Sort-Object -Unique))
  } else {
    Warn-If ($Baseline -ne "4pct") "Baseline '$Baseline' is ignored when Method=$Method."
  }

  if ($MarketMode -eq "bootstrap") {
    Warn-If ($BootstrapBlock -eq 0) "MarketMode=bootstrap but BootstrapBlock=0 (set 12 or 24)."
    $hasCsv = ($ExtraArgs | Where-Object { $_ -match '^--market_csv$' }).Count -gt 0
    Warn-If (-not $hasCsv) "bootstrap runs usually need --market_csv <path> in -ExtraArgs."
  }

  # Seeds vs SeedList
  if ($SeedList -and $SeedList.Trim().Length -gt 0) {
    $tmp = $SeedList -split "," | Where-Object { $_ -match '^\s*\d+\s*$' } | ForEach-Object { [int]$_ }
    if ($tmp.Count -eq 0) { throw "Invalid -SeedList. Use comma-separated integers." }
  } else {
    if ($Seeds -le 0) { throw "-Seeds must be positive." }
  }

  if ([string]::IsNullOrWhiteSpace($Module)) { throw "Module name is empty." }

  Write-Host ("[CONF] Py={0} Module={1} OutRoot={2}" -f $pyAbs, $Module, $script:OutRoot)
}

# ─────────────────────────────────────────────────────────
# 인자 구성
# ─────────────────────────────────────────────────────────
function Get-BaseArgs {
  param([string]$OutDir,[string]$Tag,[hashtable]$Fixed)

  $args = @(
    "--method",$Method,
    $(if($Method -eq "rule"){ @("--baseline",$Baseline) } else { @() }),
    "--market_mode",$MarketMode,
    "--horizon_years","$HorizonY",
    "--alpha","$Alpha",
    "--beta","0.996",
    "--lambda_term","$Lambda",
    "--w_max","$WMax",
    "--q_floor","$QFloor",
    "--phi_adval","$Fee",
    "--outputs",$OutDir,
    "--tag",$Tag,
    "--autosave","on",
    "--quiet",$Quiet,
    "--xai_on",$XaiOn
  )

  if ($MarketMode -eq "bootstrap" -and $BootstrapBlock -in 12,24) {
    $args += @("--bootstrap_block","$BootstrapBlock")
  }

  if ($SeedList -and $SeedList.Trim().Length -gt 0) {
    $args += @("--seeds") + ($SeedList -split ",")
  } else {
    $args += @("--seeds","$Seeds")
  }
  $args += @("--n_paths","$NPaths")

  if ($EtaMode)        { $args += @("--eta_mode",$EtaMode) }
  if ($EtaBudgetHms)   { $args += @("--eta_budget_hms",$EtaBudgetHms) }
  if ($EtaBudgetS -gt 0){ $args += @("--eta_budget_s","$EtaBudgetS") }
  if ($EtaHardStop)    { $args += @("--eta_hard_stop",$EtaHardStop) }
  if ($EtaDb)          { $args += @("--eta_db",$EtaDb) }

  foreach ($k in $Fixed.Keys) { $args += @("--$k", "$($Fixed[$k])") }
  if ($ExtraArgs.Count -gt 0) { $args += $ExtraArgs }

  return ,$args
}

# ─────────────────────────────────────────────────────────
# 실행
# ─────────────────────────────────────────────────────────
function Invoke-Run {
  param([string]$Tag,[string]$OutDir,[hashtable]$Fixed = @{})

  $metricsPath = Join-Path $OutDir "_logs\metrics.csv"
  if ($SkipExisting -and (Test-Path $metricsPath)) {
    Write-Host "[SKIP] $Tag (found: $metricsPath)"
    $global:RunStats.Done++; Print-ETA; return
  }

  Ensure-Dir $OutDir
  $args = Get-BaseArgs -OutDir $OutDir -Tag $Tag -Fixed $Fixed
  $shim = Ensure-Shim
  $pyAbs = Ensure-Python

  $sw = [System.Diagnostics.Stopwatch]::StartNew()
  $started = Get-Date
  Write-Host "[RUN ] $Tag -> $OutDir (`$Method=$Method)"

  # 안전 실행: ProcessStartInfo
  $argList = @($shim, $Module) + $args
  $quoted = $argList | ForEach-Object {
    if ($_ -is [string] -and $_.Contains(' ')) { '"' + $_ + '"' } else { $_ }
  }
  $psi = New-Object System.Diagnostics.ProcessStartInfo
  $psi.FileName = $pyAbs
  $psi.Arguments = ($quoted -join ' ')
  $psi.RedirectStandardOutput = $false
  $psi.RedirectStandardError  = $false
  $psi.UseShellExecute = $true

  $rc = 1
  try {
    $proc = [System.Diagnostics.Process]::Start($psi)
    $proc.WaitForExit()
    $rc = [int]$proc.ExitCode
  } catch {
    Write-Host "[ERR ] failed to start python: $($_.Exception.Message)" -ForegroundColor Red
    $rc = 1
  }

  $sw.Stop()
  $ended = Get-Date

  $global:RunStats.Durations += $sw.Elapsed.TotalSeconds
  $global:RunStats.Done++
  if ($rc -ne 0) {
    $global:RunStats.Fail++
    Write-Host ("[FAIL] {0} rc={1} took={2}" -f $Tag, $rc, $sw.Elapsed.ToString()) -ForegroundColor Red
  } else {
    $global:RunStats.Ok++
    Write-Host ("[DONE] {0} took={1}" -f $Tag, $sw.Elapsed.ToString()) -ForegroundColor Green
  }

  $row = @{
    timestamp = $ended.ToString("s")
    method    = $Method
    tag       = $Tag
    outdir    = $OutDir
    rc        = $rc
    took_s    = [int]$sw.Elapsed.TotalSeconds
    took_hms  = $sw.Elapsed.ToString()
    seeds     = $Seeds
    seed_list = $SeedList
    n_paths   = $NPaths
    module    = $Module
    commit    = ""   # version_info 유틸 도입 시 채움
    version   = "night_run.ps1 v9 (shim fallback+summary safe-cast+plan dedup)"
  }
  Append-DecisionLog -OutRootLocal $script:OutRoot -Row $row
  Print-ETA
}

